func TestRenderPlanMarkdown_OmitsEmptyBlocks(t *testing.T) {
	proposal := LaneProposal{
		Goal:     "tiny goal",
		Sessions: []Lane{{Label: "session-1", Scope: "do a thing"}},
	}
	out := RenderPlanMarkdown(proposal)
	for _, unwanted := range []string{
		"Work to do:",
		"Files (own):",
		"Files (avoid):",
		"**Notes:**",
		"(depends:",
	} {
		if strings.Contains(out, unwanted) {
			t.Errorf("output should not contain %q for an empty-field lane\n--- output ---\n%s", unwanted, out)
		}
	}
	if !strings.Contains(out, "## session-1\n") {
		t.Errorf("expected bare `## session-1` heading without depends clause:\n%s", out)
	}
	if !strings.Contains(out, "**do a thing**") {
		t.Errorf("scope still rendered:\n%s", out)
	}
}

